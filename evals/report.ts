// ============================================================================
// Report rendering — turns raw runs into evals/RESULTS.md
// ----------------------------------------------------------------------------
// Recommendation rule ("cheapest model that HOLDS red-flag recall"):
//   A model qualifies if its aggregate high-severity recall is
//     (a) >= ABS_FLOOR in absolute terms, AND
//     (b) within REL_TOLERANCE of the best-performing model.
//   Among qualifying models, pick the cheapest (lowest corpus cost).
// ============================================================================

import { PRICING, MODELS } from './corpus';
import type { RecallResult } from './score';

export interface RunRecord {
  model: string;
  contractId: string;
  contractKind: string;
  inputTokens: number;
  outputTokens: number;
  costUsd: number;
  redFlagCount: number;
  stopReason: string | null;
  error?: string;
  score: RecallResult;
}

const ABS_FLOOR = 0.85; // must catch at least 85% of high-severity flags
const REL_TOLERANCE = 0.05; // and be within 5 points of the best model

interface ModelAgg {
  model: string;
  label: string;
  totalGold: number;
  totalSurfaced: number;
  totalMarkedHigh: number;
  recall: number;
  recallHigh: number;
  totalCost: number;
  avgCost: number;
  runs: number;
  errors: number;
}

function aggregate(runs: RunRecord[]): ModelAgg[] {
  return MODELS.map((model) => {
    const mine = runs.filter((r) => r.model === model);
    const totalGold = mine.reduce((s, r) => s + r.score.total, 0);
    const totalSurfaced = mine.reduce((s, r) => s + r.score.surfaced, 0);
    const totalMarkedHigh = mine.reduce((s, r) => s + r.score.markedHigh, 0);
    const totalCost = mine.reduce((s, r) => s + (Number.isFinite(r.costUsd) ? r.costUsd : 0), 0);
    const errors = mine.filter((r) => r.error).length;
    return {
      model,
      label: PRICING[model]?.label ?? model,
      totalGold,
      totalSurfaced,
      totalMarkedHigh,
      recall: totalGold ? totalSurfaced / totalGold : 0,
      recallHigh: totalGold ? totalMarkedHigh / totalGold : 0,
      totalCost,
      avgCost: mine.length ? totalCost / mine.length : 0,
      runs: mine.length,
      errors,
    };
  });
}

function pct(x: number): string {
  return `${(x * 100).toFixed(0)}%`;
}

function recommend(aggs: ModelAgg[]): { pick: ModelAgg; rationale: string } {
  const best = Math.max(...aggs.map((a) => a.recall));
  const qualifying = aggs.filter(
    (a) => a.errors === 0 && a.recall >= ABS_FLOOR && a.recall >= best - REL_TOLERANCE,
  );
  // MODELS is ordered cheapest -> most expensive, so first qualifying is cheapest.
  const ordered = MODELS.map((m) => qualifying.find((q) => q.model === m)).filter(Boolean) as ModelAgg[];
  if (ordered.length === 0) {
    // Nobody clears the bar: fall back to the highest-recall model.
    const top = [...aggs].sort((a, b) => b.recall - a.recall)[0];
    return {
      pick: top,
      rationale:
        `No model cleared the ${pct(ABS_FLOOR)} absolute floor. Falling back to the ` +
        `highest-recall model (${top.label}, ${pct(top.recall)}). Treat with caution — ` +
        `consider enabling extended thinking or improving the prompt before shipping.`,
    };
  }
  const pick = ordered[0];
  return {
    pick,
    rationale:
      `${pick.label} is the cheapest model that holds recall: ${pct(pick.recall)} ` +
      `high-severity recall (best was ${pct(best)}), at $${pick.totalCost.toFixed(4)} ` +
      `across the corpus (~$${pick.avgCost.toFixed(5)}/analysis). It clears the ` +
      `${pct(ABS_FLOOR)} floor and is within ${pct(REL_TOLERANCE)} of the best model.`,
  };
}

export function renderResults(runs: RunRecord[]): string {
  const aggs = aggregate(runs);
  const { pick, rationale } = recommend(aggs);
  const generatedNote =
    'Generated by `evals/run.ts`. Re-run with `ANTHROPIC_API_KEY=... npx tsx run.ts`.';

  const lines: string[] = [];
  lines.push('# ClearSign — Analysis Model Eval Results');
  lines.push('');
  lines.push('> ' + generatedNote);
  lines.push('');
  lines.push('**Goal:** recommend the cheapest Claude model that holds high-severity');
  lines.push('red-flag recall for freelance contract analysis.');
  lines.push('');

  // Recommendation up top.
  lines.push('## Recommendation');
  lines.push('');
  lines.push('```');
  lines.push(`ANALYSIS model id: ${pick.model}`);
  lines.push('```');
  lines.push('');
  lines.push(rationale);
  lines.push('');
  lines.push('Lane A should set this id in `api/_lib/anthropic.ts` (this lane is advisory and does not edit it).');
  lines.push('');

  // Per-model comparison.
  lines.push('## Per-model comparison');
  lines.push('');
  lines.push('| Model | High-sev recall | Severity-correct recall | Gold caught | Corpus cost | Avg cost/analysis | Errors |');
  lines.push('|---|---|---|---|---|---|---|');
  for (const a of aggs) {
    lines.push(
      `| ${a.label} (\`${a.model}\`) | ${pct(a.recall)} | ${pct(a.recallHigh)} | ` +
        `${a.totalSurfaced}/${a.totalGold} | $${a.totalCost.toFixed(4)} | ` +
        `$${a.avgCost.toFixed(5)} | ${a.errors} |`,
    );
  }
  lines.push('');
  lines.push('- **High-sev recall**: gold high-severity flags surfaced as any red flag.');
  lines.push('- **Severity-correct recall**: surfaced *and* marked `high` (severity calibration).');
  lines.push('- **Cost**: actual token usage × published per-MTok pricing (Haiku 1/5, Sonnet 3/15, Opus 5/25).');
  lines.push('');

  // Per-contract breakdown.
  lines.push('## Per-contract recall');
  lines.push('');
  const contractIds = Array.from(new Set(runs.map((r) => r.contractId)));
  const header = ['Contract', ...MODELS.map((m) => PRICING[m]?.label ?? m)];
  lines.push(`| ${header.join(' | ')} |`);
  lines.push(`|${header.map(() => '---').join('|')}|`);
  for (const cid of contractIds) {
    const kind = runs.find((r) => r.contractId === cid)?.contractKind ?? cid;
    const cells = MODELS.map((m) => {
      const r = runs.find((x) => x.contractId === cid && x.model === m);
      if (!r) return '—';
      if (r.error) return 'err';
      return `${pct(r.score.recall)} (${r.score.surfaced}/${r.score.total})`;
    });
    lines.push(`| ${kind} | ${cells.join(' | ')} |`);
  }
  lines.push('');

  // Missed-flag detail for the recommended model.
  lines.push(`## Missed high-severity flags — ${pick.label} (recommended)`);
  lines.push('');
  const misses = runs
    .filter((r) => r.model === pick.model)
    .flatMap((r) =>
      r.score.outcomes.filter((o) => !o.surfaced).map((o) => `- [${r.contractId}] ${o.label}`),
    );
  if (misses.length === 0) {
    lines.push('_None — recommended model surfaced every gold high-severity flag._');
  } else {
    lines.push(...misses);
  }
  lines.push('');

  lines.push('## Method');
  lines.push('');
  lines.push('- 4 freelance contracts (Mutual NDA, design SOW, IC agreement with aggressive');
  lines.push('  IP assignment, freelance MSA), each hand-labeled with its genuinely');
  lines.push('  high-severity red flags (`evals/corpus.ts`).');
  lines.push('- Each contract analyzed by each model using the production analyze prompt +');
  lines.push('  `ContractAnalysis` JSON schema (`evals/analyzePrompt.ts`), structured outputs,');
  lines.push('  `max_tokens: 8000`, no extended thinking (cost-representative baseline).');
  lines.push('- Recall scored by deterministic concept matchers (`evals/score.ts`): a gold');
  lines.push('  flag counts as recalled if a returned red flag matches its concept.');
  lines.push('- One run per (contract, model). For a tighter estimate, run multiple seeds and');
  lines.push('  average — wording varies between runs, though concept recall is stable.');
  lines.push('');
  return lines.join('\n');
}
